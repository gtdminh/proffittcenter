    public Calendar execute(Calendar date,String title){
        setTitle(title);
        Audio.play("Tada");
        this.date=date;
        if(date==null){
            this.date=Calendar.getInstance();
        } else {
            
        }
        dateChooser.setCalendar(this.date);
        returnDate=null;
        setVisible(true);
        return returnDate;
    }
    
