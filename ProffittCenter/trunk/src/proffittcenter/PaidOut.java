    void execute(boolean out,boolean noSale) {
        this.noSale=noSale;
        try {
            if(out&&!noSale){
                setTitle(java.util.ResourceBundle.getBundle("proffittcenter/resource/PaidOut").getString("PaidOut.title"));
            }else if(!noSale){
                setTitle(java.util.ResourceBundle.getBundle("proffittcenter/resource/PaidOut").getString("PaidIn"));
            } else {
                setTitle(java.util.ResourceBundle.getBundle("proffittcenter/resource/PaidOut").getString("noSale"));
            }
            this.out=out;
            jReason.removeAllItems();
            jReason.addItem("");
            PreparedStatement paidOutsQueryOut = Main.getConnection().prepareStatement(
                    "SELECT DISTINCT(UPPER(Description)) AS Description FROM PaidOuts " +
                    "WHERE Amount >0 ORDER BY  Description ");
            PreparedStatement paidOutsQueryIn = Main.getConnection().prepareStatement(
                    "SELECT DISTINCT(UPPER(Description)) AS Description FROM PaidOuts " +
                    "WHERE Amount <0 ORDER BY  Description ");
            PreparedStatement noSaleQuery = Main.getConnection().prepareStatement(
                    "SELECT DISTINCT(UPPER(Description)) AS Description FROM PaidOuts " +
                    "WHERE Amount =0 ORDER BY  Description ");
            if(out&&!noSale){
                rs = paidOutsQueryOut.executeQuery();
            }else if(!out && !noSale){
                rs = paidOutsQueryIn.executeQuery();
            }else {
                rs = noSaleQuery.executeQuery();
            }
            int i = 0;
            if (rs.first()) {
                //Description to display
                do {
                    //Description to display
                    String S = StringOps.firstCaps(rs.getString("Description").toLowerCase());
                    jReason.addItem(S.trim());
                } while (rs.next() && i < 7);
            }
            paidOutsQueryOut.close();
            paidOutsQueryIn.close();
        } catch (SQLException ex) {
            Audio.play("Ring");
        }
        amountTestField.setText("0");
        Audio.play("TaDa");
        jReason.setEditable(true);
        jReason.requestFocus();
        amountTestField.setFocusable(!noSale);
        amountTestField.setVisible(!noSale);
        jLabel2.setVisible(!noSale);
        getRootPane().setDefaultButton(okBtn);
        setVisible(true);
    }

