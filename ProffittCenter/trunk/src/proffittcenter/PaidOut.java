    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        //Save the data or return to screen if faulty
        int n=0;
        String reason = (String) jReason.getSelectedItem();
        if (reason.isEmpty()) {
            Audio.play("Ring");
            jReason.requestFocus();
            return;
        }
        if(!noSale){
            amountString = StringOps.numericOnly(amountTestField.getText());
            n=Integer.parseInt(amountString);
            if (amountString.isEmpty()||n<=0) {
                Audio.play("Ring");
                amountTestField.requestFocus();
                amountTestField.selectAll();
                return;
            }
        }
        //now save
        try {
            PreparedStatement np = Main.getConnection().prepareStatement(
                    "INSERT INTO " +
                    " PaidOuts(ID,Description,Amount,TillID,Operator) " +
                    " VALUES(?,?,?,?,?)");
            np.setNull(1, Types.INTEGER);//ID
            String s=StringOps.firstCaps((String) jReason.getSelectedItem());
            np.setString(2, s.trim());//description
            if(out&&!noSale){
                np.setString(3, amountString);//price
            } else if(!noSale){
                np.setString(3, "-"+amountString);//price
            } else {
                np.setInt(3, 0);
            }
            np.setInt(4, Main.shop.getTillId());
            np.setInt(5, Main.operator.getOperator());
            np.executeUpdate();
            np.close();
            Main.receiptPrinter.cashDrawerOpen();
            //now print a receipt
            Main.receiptPrinter.startReceipt();
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            java.util.Date date = new java.util.Date();
            Main.receiptPrinter.printLine(Main.operator.operatorName);
            Main.receiptPrinter.printLine(bundle.getString("CurrentDateTime")+ " "+ dateFormat.format(date));
            Main.receiptPrinter.printLine(bundle.getString("TillNo")+ " "+Main.shop.getTillId());
            if(out&&!noSale){
                Main.receiptPrinter.printLine(bundle.getString("PaidOut"));
            } else if(!noSale){
                Main.receiptPrinter.printLine(bundle.getString("PaidIn"));
            } else {
                Main.receiptPrinter.printLine(bundle.getString("noSale"));
            }
            Main.receiptPrinter.printLine(s);//reason
            if(!noSale){
                Main.receiptPrinter.printLine(new Money(n).toString());
            }
            Main.receiptPrinter.endReceipt();
            Audio.play("Beep");
        } catch (SQLException ex) {
            Audio.play("Ring");
            System.out.println(bundle.getString("SQL_Error") + ex.getMessage());
        }
        setVisible(false);
    }//GEN-LAST:event_okBtnActionPerformed

