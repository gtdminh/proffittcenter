    public void execute(String title, PreparedStatement ps) {
        this.ps = ps;
        this.setTitle(title);
        jTable1.clearSelection();
        rowData.clear();
        Calendar c = Calendar.getInstance();
        toDateChooser.setCalendar(c);
        c.add(Calendar.DAY_OF_MONTH, -1);
        fromDateChooser.setCalendar(c);
        jTable1.addNotify();
        Audio.play("Tada");
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        setVisible(true);
    }

