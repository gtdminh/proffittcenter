    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
//            okButton.setEnabled(false);
//            this.repaint();
            Calendar startDate = fromDateChooser.getCalendar();
            Calendar endDate = toDateChooser.getCalendar();
            java.sql.Date sd = new java.sql.Date(startDate.getTimeInMillis());//start
            ps.setDate(1, sd);
            java.sql.Date ed = new java.sql.Date(endDate.getTimeInMillis());//end
            ps.setDate(2, ed);
            ResultSet rs = ps.executeQuery();
            tableHeader.clear();
            ResultSetMetaData rsmd = (ResultSetMetaData) ps.getMetaData();
            numberOfColumns = rsmd.getColumnCount();
            tableHeader.clear();
            for (int i = 0; i < numberOfColumns && i < 4; i++) {
                @SuppressWarnings("unchecked")
                boolean b0 = tableHeader.add(rsmd.getColumnLabel(i + 1));
            }
            jTable1.setModel(model);
            model.fireTableDataChanged();
            rowData.clear();
            while (rs.next()) {
                String[] dd = {"", "", "", ""};//maximum of four columns
                for (int i = 0; i < numberOfColumns && i < 4; i++) {
                    dd[i] = rs.getString(i + 1);
                }
                line = dd;
                rowData.add(line);
            }
            rs.close();
//            okButton.setEnabled(true);
            model.fireTableDataChanged();
            Audio.play("Beep");
        } catch (SQLException ex) {
            Main.logger.log(Level.SEVERE, "SalesBy ", "SQLException: " + ex.getMessage());
            Audio.play("Ring");
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
}//GEN-LAST:event_okButtonActionPerformed

