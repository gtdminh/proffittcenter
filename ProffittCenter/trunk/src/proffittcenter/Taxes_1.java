    AbstractTableModel model = new AbstractTableModel() {

        @Override
        public String getColumnName(int col) {
            return (String) tableHeader.get(col);
        }

        @Override
        public int getColumnCount() {
            return tableHeader.size();
        }

        @Override
        public int getRowCount() {
            return rowData.size();
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            line = rowData.get(rowIndex);
            if (columnIndex == 0) {//id
                return line[0];
            } else if (columnIndex == 1) {//description
                return line[1];
            } else if (columnIndex == 2) {//rate
                return (PerCent)line[2];
//                return (StringOps.asPerCent((Integer) line[2]));
            } else if (columnIndex == 3) {//symbol
                return (String) line[3];
            } else {
                return null;
            }
        }

        @Override
        public Class getColumnClass(int c) {
            return getValueAt(0, c).getClass();
        }
    };

