    private void drawGrid() {
        try {
            jTable1.clearSelection();
            rowData.clear();
            PreparedStatement taxes;
            taxes = Main.getConnection().prepareStatement(SQL.taxes );
            ResultSet rs = taxes.executeQuery();
            while (rs.next()) {//
                id = rs.getInt("ID");
                description = rs.getString("Description");
                rate = new PerCent(rs.getInt("Rate"));
                symbol = rs.getString("Symbol");
                Object[] line1 = {id,description, rate, symbol};
                rowData.add(line1);
                model.fireTableDataChanged();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Taxes_1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

