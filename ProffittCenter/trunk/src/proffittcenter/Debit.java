    int execute(int total,boolean cashBack) {
        amount = total;
        valueT.setText((new Money(total).toString()));
        if (cashBack) {
            cashBackT.setVisible(true);
            jLabel3.setVisible(true);
            cashBackT.setText((new Money(0).toString()));
        } else {
            cashBackT.setText((new Money(0).toString()));
            cashBackT.setVisible(false);
            jLabel3.setVisible(false);
        }
        valueT.selectAll();
        valueT.requestFocus();
        Audio.play("TaDa");
        paid = 0;
        setVisible(true);
        return paid;
    }

