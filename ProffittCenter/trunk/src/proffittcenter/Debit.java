    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        paid = Integer.parseInt(StringOps.numericOnly(valueT.getText()));
        if (amount < paid) {
            Audio.play("Ring");
            valueT.setText((new Money(amount)).toString());
            valueT.selectAll();
            valueT.requestFocus();
            return;
        }
        paid += Integer.parseInt(StringOps.numericOnly(cashBackT.getText()));
        Audio.play("Beep");
        setVisible(false);
}//GEN-LAST:event_okBtnActionPerformed

