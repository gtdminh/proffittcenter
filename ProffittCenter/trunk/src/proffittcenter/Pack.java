    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            // save ammended values
            statement = Main.getConnection().prepareStatement(setPack);
            size=Integer.parseInt(sizeTextField.getText());
            statement.setInt(1, size);
            code=codeTextField.getText();
            statement.setString(2, code);
            encoded=encodedComboBox.getSelectedIndex();
            statement.setInt(3, encoded);
            statement.setInt(4, pack);
            statement.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Pack.class.getName()).log(Level.SEVERE, null, ex);
            Audio.play("Ring");
            return;
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

