    void execute(int pack) {
        try {
            this.pack = pack;
            idTextField.setText(""+pack);
            statement = Main.getConnection().prepareStatement(getPack);
            statement.setInt(1, pack);
            rs=statement.executeQuery();
            if(rs.first()){
                whenCreated= rs.getDate("WhenCreated");
                size = rs.getInt("Size");
                code = rs.getString("Code");
                Encoded = rs.getInt("Encoded");
            }
            rs.close();
            whenTextField.setText(whenCreated.toString());
            sizeTextField.setText(""+size);
            codeTextField.setText(code);
            encodedComboBox.setSelectedIndex(Encoded);
            setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(Pack.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
