    int execute() {
        Audio.play("Tada");
        int i=0,j=0;
        selectedSupplier=-1;
        supplierCombo.removeAllItems();
        try {
            PreparedStatement departmentQuery =
                    Main.getConnection().prepareStatement(
                    "SELECT Description FROM Suppliers " +
                    "ORDER BY Description");
            ResultSet rs = departmentQuery.executeQuery();
            while (rs.next()) {
                //suppliers to display
                String S = rs.getString("Description");
                supplierCombo.addItem(S);
                if(S.equalsIgnoreCase( root.get("selectedSupplierDescription", ""))){
                        j=i;
                    }
                i++;
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(SelectSupplier.class.getName()).log(Level.SEVERE, null, ex);
        }
        supplierCombo.setSelectedIndex(j);
        supplierCombo.requestFocus();
        //FormRestore.createPosition(this, root);
        selectedSupplier=-1;
        setVisible(true);
        //FormRestore.destroyPosition(this, root);
        root.put("selectedSupplierDescription",(String) supplierCombo.getSelectedItem());
        return selectedSupplier;
    }

    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        String S = (String)supplierCombo.getSelectedItem();
        selectedSupplier=0;
        if(!S.isEmpty()){
            try {
                PreparedStatement supplierQuery =
                        Main.getConnection().prepareStatement(
                        "SELECT ID,Description FROM Suppliers " +
                        "WHERE Description=?");
                supplierQuery.setString(1, S);
                ResultSet rs;
                rs = supplierQuery.executeQuery();
                if (rs.first()) {
                    //not if empty
                    selectedSupplier = rs.getInt("ID");
                }
                rs.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        this.setVisible(false);
}//GEN-LAST:event_okBtnActionPerformed

    private void okBtnKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_okBtnKeyReleased
    String S = (String) supplierCombo.getSelectedItem();
        selectedSupplier = 0;
        if (!S.isEmpty()) {
            try {
                PreparedStatement supplierQuery =
                        Main.getConnection().prepareStatement(
                        "SELECT ID,Description FROM Suppliers "
                        + "WHERE Description=?");
                supplierQuery.setString(1, S);
                ResultSet rs;
                rs = supplierQuery.executeQuery();
                if (rs.first()) {
                    //not if empty
                    selectedSupplier = rs.getInt("ID");
                } else {
                    selectedSupplier = 0;
                }
                rs.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        getRootPane().setDefaultButton(okBtn);
        this.setVisible(false);
}//GEN-LAST:event_okBtnKeyReleased

