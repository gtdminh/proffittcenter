    public int execute(String department) {
        this.department = department;
        Audio.play("Tada");
        jQuantity.setValue(0);
        jMinimum.setValue(0);
        jShelfRent.setValue(0);
        departmentComboBox.removeAllItems();
        departmentComboBox.addItem(department);
        jTypeComboBox.removeAllItems();
        jTypeComboBox.addItem(type);
        try {
            PreparedStatement departmentQuery = Main.getConnection().prepareStatement(
                    "SELECT Departments.*,Departments.Supplier,Suppliers.Description,Taxes.Description " +
                    "FROM Departments,Taxes,Suppliers " +
                    "WHERE Departments.Tax=Taxes.ID "+
                    "AND Suppliers.ID=Departments.Supplier " +
                    "ORDER BY Departments.Description");
            ResultSet rs = departmentQuery.executeQuery();
            if (rs.first()) {//not if empty
                do {//departments to display
                    String S = rs.getString("Departments.Description");
                    departmentComboBox.addItem(S);
                    if (S.equals(department)) {
                        tax1 = rs.getString("Taxes.Description");
                    }
                } while (rs.next());
            }
            rs.close();
            departmentComboBox.setSelectedItem((Object) department);
            //fill the items from the taxes table
            PreparedStatement taxQuery =
                    Main.getConnection().prepareStatement(
                            "SELECT Description FROM TAXES ORDER BY ID");
            ResultSet rst = taxQuery.executeQuery();
            tax1ComboBox.removeAllItems();
            tax2ComboBox.removeAllItems();
            while (rst.next()) {
                tax1ComboBox.addItem(rst.getString("Description"));
                tax2ComboBox.addItem(rst.getString("Description"));
            }
            rst.close();
            tax1ComboBox.setSelectedItem((Object) tax1);
            tax2ComboBox.setSelectedItem((Object) tax2);
            type = 0;
            jTypeComboBox.removeAllItems();
            //fill in all possible kinds
            for (short cnt = 0; cnt < SkuType.size(); cnt++) {
                String s = SkuType.description(cnt);
                jTypeComboBox.addItem(s);
                if (SkuType.description(cnt).
                        equalsIgnoreCase(skuTypeBundle.getString("Included"))) {
                    skuType = s;
                    jTypeComboBox.setSelectedIndex(cnt);
                }
            }
        } catch (SQLException ex) {
            Audio.play("Ring");
            ex.printStackTrace();
        }
        jQuantity.requestFocus();
//        //FormRestore.createPosition(this, root);
        sku=-1;
        setVisible(true);
        return sku;
    }

