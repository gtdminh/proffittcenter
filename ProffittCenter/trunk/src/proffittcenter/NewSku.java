    private boolean saveSkuDetails() {
        int s = 1;
        int ty = 1;
        int t = 1;
        int d = 1;
        department = (String) departmentComboBox.getSelectedItem();
        if (department.isEmpty()) {
            department = "Default";
        }
        tax1 = (String) tax1ComboBox.getSelectedItem();
        tax2 = (String) tax2ComboBox.getSelectedItem();
        if(tax2==null){
            tax2= (String) tax2ComboBox.getItemAt(0);
        }
        type = (short) jTypeComboBox.getSelectedIndex();
        try {
            PreparedStatement np = Main.getConnection().prepareStatement(
                    "INSERT INTO " +
                    " Skus(ID,Department,Tax,Quantity,Minimum,StockType," +
                    "ShelfRent,Tax2) VALUES(?," +
                    " (SELECT ID FROM Departments WHERE Description=?)," +
                    "(SELECT ID FROM Taxes WHERE Description=?)," +
                    "?,?,?," +
                    "?,(SELECT ID FROM Taxes WHERE Description=?))");
            np.setNull(1, Types.INTEGER);//ID
            np.setString(2, department);//department is a string!
            np.setString(3, tax1);//tax
            np.setInt(4, (Integer) jQuantity.getValue());//quantity
            np.setInt(5, (Integer)jMinimum.getValue());//minimum
            np.setShort(6, type);
            np.setInt(7, (Integer)jShelfRent.getValue());
            np.setString(8, tax2);//tax
            np.executeUpdate();
            np.close();
            PreparedStatement np1 = Main.getConnection().prepareStatement(
                    "SELECT LAST_INSERT_ID() FROM Skus");
            ResultSet rs = np1.executeQuery();
            rs.first();
            sku = rs.getInt(1);
            rs.close();
        } catch (SQLException ex) {
            Audio.play("Ring");
            Logger.getLogger(NewSku.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
        return true;
    }
